    {
        float isoLevel = 0f;
        if (Mathf.Abs(isoLevel - valp1) < 0.00001f) return p1;
        if (Mathf.Abs(isoLevel - valp2) < 0.00001f) return p2;
        if (Mathf.Abs(valp1 - valp2) < 0.00001f) return p1;

        float mu = (isoLevel - valp1) / (valp2 - valp1);
        return p1 + mu * (p2 - p1);
    }

