    // this function generates a procedural terrain of voxels using marching cubes from the voxel data structure
    public void GenerateMeshMarchingCubes(int sizeX, int sizeY, int sizeZ) 
    {
        meshData.ClearData();
        Vector3 blockPos = new Vector3(0, 0, 0);
        Voxel block = new Voxel() { ID = 1 };

        Vector3[] faceVertices = new Vector3[4];
        Vector2[] faceUvs = new Vector2[15];

        VoxelColour voxelColor;
        Color voxelColorAlpha;
        Vector2 voxelSmoothness;

        // Loop over each cube in the grid
        for (int x = 0; x < sizeX; x++)
        {
            for (int y = 0; y < sizeY; y++)
            {
                for (int z = 0; z < sizeZ; z++)
                {
                    Vector3 cubePos = new Vector3(x, y, z);

                    // Sample the 8 corners of the cube
                    float[] cube = new float[8];
                    Vector3[] cornerPositions = new Vector3[8];
                    for (int i = 0; i < 8; i++)
                    {
                        Vector3 corner = cubePos + voxelVertices[i];
                        cornerPositions[i] = corner;
                        cube[i] = GetDensity(corner);
                    }

                    // Calculate cube index
                    int cubeIndex = 0;
                    for (int i = 0; i < 8; i++)
                        if (cube[i] < 0) cubeIndex |= (1 << i);

                    // No triangles for this cube
                    if (edgeTable[cubeIndex] == 0)
                        continue;

                    // Find the vertices where the surface intersects the cube
                    Vector3[] vertList = new Vector3[12];
                    for (int i = 0; i < 12; i++)
                    {
                        if ((edgeTable[cubeIndex] & (1 << i)) != 0)
                        {
                            int a0 = edgeConnection[i, 0];
                            int b0 = edgeConnection[i, 1];
                            vertList[i] = VertexInterp(cornerPositions[a0], cornerPositions[b0], cube[a0], cube[b0]);
                        }
                    }
                    // Get the voxel at the cube position to determine color
                    voxelColor = WorldManager.Instance.worldColors[block.ID - 1];
                    voxelColorAlpha = voxelColor.color;
                    voxelColorAlpha.a = 1;
                    voxelSmoothness = new Vector2(voxelColor.metallic, voxelColor.smoothness);

                    // Create triangles
                    for (int i = 0; triTable[cubeIndex, i] != -1; i += 3)
                    {
                        int idx0 = triTable[cubeIndex, i];
                        int idx1 = triTable[cubeIndex, i + 1];
                        int idx2 = triTable[cubeIndex, i + 2];

                        int vertIndex = meshData.vertices.Count;
                        meshData.vertices.Add(vertList[idx0]);
                        meshData.vertices.Add(vertList[idx1]);
                        meshData.vertices.Add(vertList[idx2]);
                        meshData.triangles.Add(vertIndex);
                        meshData.triangles.Add(vertIndex + 1);
                        meshData.triangles.Add(vertIndex + 2);

                        // Optionally add colors/uvs here
                        meshData.colors.Add(voxelColorAlpha);
                        meshData.colors.Add(voxelColorAlpha);
                        meshData.colors.Add(voxelColorAlpha);
                        meshData.uvs.Add(faceUvs[i]);
                        meshData.uvs.Add(faceUvs[i]);
                        meshData.uvs.Add(faceUvs[i]);
                        meshData.UVs2.Add(voxelSmoothness);
                        meshData.UVs2.Add(voxelSmoothness);
                        meshData.UVs2.Add(voxelSmoothness);
                    }
                }
            }
        }
    }
    // this function generates a procedural terrain of cubes/voxels when not using marching cubes
    public void GenerateMesh() // called to generate the mesh data from the voxel data
    {
        meshData.ClearData();

        Vector3 blockPos = new Vector3(0,0,0);
        Voxel block = new Voxel() { ID = 1 };

        int counter = 0;
        Vector3[] faceVertices = new Vector3[4];
        Vector2[] faceUvs = new Vector2[4];

        VoxelColour voxelColor;
        Color voxelColorAlpha;
        Vector2 voxelSmoothness;

        foreach (KeyValuePair<Vector3, Voxel> item in data) // iterate through each voxel in the container
        {
            if (item.Value.ID == 0) { continue; } // skip empty voxels
            blockPos = item.Key;
            block = item.Value;

            voxelColor = WorldManager.Instance.worldColors[block.ID - 1];
            voxelColorAlpha = voxelColor.color;
            voxelColorAlpha.a = 1;
            voxelSmoothness = new Vector2(voxelColor.metallic, voxelColor.smoothness);

            //iterate through each face of the voxel
            for (int f = 0; f < 6; f++)
            {
                if (this[blockPos + voxelFaceChecks[f]].isSolid) { continue; } // skip faces that are solid as dont need to draw it

                //iterate through each vertex of the face
                for (int v = 0; v < 4; v++)
                {
                    faceVertices[v] = voxelVertices[voxelVertexIndex[f, v]] + blockPos;
                    faceUvs[v] = voxelUVs[v];
                }
                //add the face vertices and uvs to the mesh data
                int vertIndex = meshData.vertices.Count;
                for (int i = 0; i < 4; i++)
                {
                    meshData.vertices.Add(faceVertices[i]);
                    meshData.uvs.Add(faceUvs[i]);

                    meshData.colors.Add(voxelColorAlpha);
                    meshData.UVs2.Add(voxelSmoothness);
                }
                //add the face triangles to the mesh data
                for (int t = 0; t < 6; t++)
                {
                    meshData.triangles.Add(vertIndex + voxelTris[f, t]);
                }
                counter++;
            }
        }
    }

    public void UploadMesh() // called to upload the mesh data to the mesh
    {
        meshData.UploadMesh();

        if (meshRenderer == null) { ConfigComponents(); } // in case this is called before config components

        meshFilter.mesh = meshData.mesh;
        if (meshData.vertices.Count > 3) { meshCollider.sharedMesh = meshData.mesh; } // only set the collider if there are enough vertices
    }

    public Voxel this[Vector3 index] // indexer to get or set voxel data
    {
        get
        {
            if (data.ContainsKey(index))
            {
                return data[index];
            }
            else
            {
                return emptyVoxel;
            }
        }
        set
        {
            if (data.ContainsKey(index))
            {
                data[index] = value;
            }
            else
            {
                data.Add(index, value);
            }
        }
    }

    public static Voxel emptyVoxel = new Voxel() { ID = 0 }; // static empty voxel for easy access
    // this section is for saving and loading chunk data
    public ChunkData GetChunkData(Vector2Int chunkPos)
    {
        var data = new ChunkData { chunkPos = chunkPos, voxels = new List<VoxelData>() };
        foreach (var kvp in this.data) // Assuming you store voxels in a dictionary
        {
            data.voxels.Add(new VoxelData { position = kvp.Key, id = kvp.Value.ID });
        }
        return data;
    }
    // Load chunk data from ChunkData object
    public void LoadChunkData(ChunkData data)
    {
        foreach (var v in data.voxels)
        {
            this[v.position] = new Voxel { ID = v.id };
        }
    }

    // this section is for marching cubes to get inside or outside of a voxel
    private float GetDensity(Vector3 pos)
    {
        // Convert local pos to world pos
        Vector3 worldPos = containerPosition + pos;

        // Find which chunk this worldPos belongs to
        int chunkSize = WorldManager.Instance.chunkSize;
        Vector2Int chunkCoord = new Vector2Int(
            (int)(worldPos.x / chunkSize),
            (int)(worldPos.z / chunkSize)
        );
        if (worldPos.x < 0) chunkCoord.x -= 1;
        if (worldPos.z < 0) chunkCoord.y -= 1;
        Container neighborChunk;
        if (WorldManager.Instance.chunks.TryGetValue(chunkCoord, out neighborChunk))
        {
            Vector3 localPos = worldPos - neighborChunk.containerPosition;
            if (neighborChunk.data.ContainsKey(localPos) && neighborChunk.data[localPos].ID != 0)
                return -1f;
        }
        return 1f;
        /*Vector3 center = new Vector3(8, 8, 8);
        float radius = 7f;
        return (pos - center).magnitude - radius;*/
    }

    // this section is for storing mesh data
    #region Mesh Data
    public struct MeshData
    {
        public Mesh mesh;
        // using arrays for better performance over lists
        public List<Vector3> vertices;
        public List<int> triangles;
        public List<Vector2> uvs;
        public List<Vector2> UVs2;
        public List<Color> colors;

        public bool Initialised;

        public void ClearData() // called to clear or set up the arrays
        {
            if (!Initialised)
            {
                vertices = new List<Vector3>();
                triangles = new List<int>();
                uvs = new List<Vector2>();
                UVs2 = new List<Vector2>();
                colors = new List<Color>();

                Initialised = true;
                mesh = new Mesh();
            }
            else
            {
                vertices.Clear();
                triangles.Clear();
                uvs.Clear();
                UVs2.Clear();
                colors.Clear();
                mesh.Clear();
            }
        }

        public void UploadMesh(bool sharedVertices = false) // called to upload the mesh data to the mesh
        {
            mesh.SetVertices(vertices);
            mesh.SetTriangles(triangles, 0, false);
            mesh.SetColors(colors);
            
            mesh.SetUVs(0, uvs);
            mesh.SetUVs(2, UVs2);

            mesh.Optimize();

            mesh.RecalculateNormals();

            mesh.RecalculateBounds();

            mesh.UploadMeshData(false);
        }
    }
